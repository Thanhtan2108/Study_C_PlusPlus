// ban dau viet code can phai include cac thu vien can thiet
// #include <bits/stdc++.h> hoac thu vien ben duoi
#include <iostream> // thu vien nhap xuat co ban

// su dung namespace std de  don gian hoa code cac ham, cac toan tu trong C++
using namespace std;

// ham main la ham chinh cua chuong trinh va return 0 de ket thuc ham chinh
int main() {

    // lenh in ra man hinh : cout ;
    // dung toan tu << de in ra noi dung, neu khong co toan tu << thi khong in ra duoc
    //  << endl de xuong dong hoac dung \n de xuong dong
    // vi du muon ra noi dung 1 dong duy nhat
    cout << "Hello, World!";
    cout << endl;

    // vi du muon in ra 2 dong
    cout << "Bat dau hoc NNLT C++" << endl << "Buoi 1\n";

    // lenh nhap tu ban phim : cin ;
    // dung toan tu >> de nhap tu ban phim, neu khong co toan tu >> thi khong nhap duoc
    // sau moi lan nhap gia trij cho 1 bien, cac gia tri cach nhau bang dau cach hoac phim enter
    // vi du nhap gia tri cho 1 bien int a
    int a;
    cin >> a;

    // Vi du nhap gia trij ch 1 bien int b va kem theo chuoi thong bao
    int b;
    cout << "Nhap gia tri cho bien b: ";
    cin >> b;

    // Nhap gia tri cho nhieu bien int
    int x, y, z;
    cout << "Nhap gia tri cho 3 bien x, y, z: ";
    cin >> x >> y >> z;

    // return 0 de ket thuc ham
    return 0;
}